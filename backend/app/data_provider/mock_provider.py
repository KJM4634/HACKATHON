def _build_competitor_sample(region_id: str, category: str, count: int) -> list[CompetitorBusiness]:
    """실제 업체명 대신 합성 이름을 붙인 지도 표시용 샘플 목록 (최대 10개)."""
    region = _REGIONS[region_id]
    대분류, 중분류, 표준분류 = _CATEGORY_TO_INDUSTRY_LABEL.get(category, ("기타", "기타", category))
    sample_size = min(count, 10)
    return [
        CompetitorBusiness(
            상호명=f"{region.행정동명.split(' ')[-1]} {category} {i + 1}호점",
            상권업종대분류명=대분류,
            상권업종중분류명=중분류,
            상권업종소분류명=None,
            표준산업분류명=표준분류,
            경도=region.경도 + (i - sample_size / 2) * 0.0008,
            위도=region.위도 + (i % 3 - 1) * 0.0006,
            도로명주소=f"{region.시도명} {region.시군구명} (mock 샘플 주소 {i + 1})",
        )
        for i in range(sample_size)
    ]


class MockDataProvider(DataProvider):
    def list_regions(self) -> list[RegionInfo]:
        return list(_REGIONS.values())

    def get_region_info(self, region_id: str) -> RegionInfo:
        if region_id not in _REGIONS:
            raise ValueError(f"알 수 없는 region_id: {region_id}")
        return _REGIONS[region_id]

    def get_population(self, region_id: str) -> PopulationStats:
        return _POPULATION[region_id]

    def get_foot_traffic(self, region_id: str) -> list[FootTrafficByHour]:
        return [
            FootTrafficByHour(시간대=h, 평균주거인구수=res, 평균직장인구수=work, 평균방문인구수=visit)
            for h, res, work, visit in _FOOT_TRAFFIC_RAW[region_id]
        ]

    def get_consumption_by_hour(self, region_id: str) -> list[ConsumptionByHour]:
        return [
            ConsumptionByHour(시간대=h, 평균이용금액=amount, 평균이용건수=count)
            for h, amount, count in _CONSUMPTION_HOUR_RAW[region_id]
        ]

    def get_consumption_by_category(self, region_id: str) -> list[ConsumptionByCategory]:
        return [
            ConsumptionByCategory(업종대분류=cat, 평균이용금액=amount, 평균이용건수=count)
            for cat, amount, count in _CONSUMPTION_CATEGORY_RAW[region_id]
        ]

    def get_competitors(self, region_id: str, category: str) -> CompetitorSummary:
        count = _COMPETITOR_COUNTS.get(region_id, {}).get(category, _DEFAULT_COMPETITOR_COUNT)
        return CompetitorSummary(
            target_category=category,
            total_count=count,
            sample=_build_competitor_sample(region_id, category, count),
        )

    def get_closure_stats(self, region_id: str, category: str) -> ClosureStats:
        # 일반음식점표준데이터에 행정동 필드가 없어 실제 집계 불가 -> 경쟁업체 수 대비 추정치(가짜)
        active = _COMPETITOR_COUNTS.get(region_id, {}).get(category, _DEFAULT_COMPETITOR_COUNT)
        opened_last_year = max(1, round(active * 0.12))
        closed_last_year = max(1, round(active * 0.09))
        closure_rate = round(closed_last_year / (active + closed_last_year) * 100, 1)
        return ClosureStats(
            업태구분명=category,
            영업중_점포수=active,
            최근1년_신규개업_수=opened_last_year,
            최근1년_폐업_수=closed_last_year,
            폐업률=closure_rate,
        )
